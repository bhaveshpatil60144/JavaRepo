package javaArray;

public class ArraySimple {
    public static void main(String[] args) {

        //Length Method
        //Lenth Keyword

        int[] a ={1, 2, 3, 4, 5, 6};
        String[] define = {"Test", "Check", "Hi", "Hello", "Nice"};
        String s = "Bhavesh Chavan";

        System.out.println(a.length);           //Length Keyword
        System.out.println(define.length);      //Length Keyword For Array Size
        System.out.println(s.length());         //Length Method for String Size
    }
}
